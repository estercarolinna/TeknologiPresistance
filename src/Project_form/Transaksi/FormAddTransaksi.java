    public FormAddTransaksi() {
        initComponents();

        // Salary item
        daoDaftarTagian = new DaoDaftarTagian();
        List<DaftarTagian> daftarTagians = daoDaftarTagian.finds();
        String types[] = new String[daftarTagians.size()];
        int index = 0;

        for (DaftarTagian rec : daftarTagians) {
            types[index] = rec.getId() + " . " + rec.getTagihan();
            ++index;
        }
        DefaultComboBoxModel model = new DefaultComboBoxModel(types);
        cmb_daftartagihan.setModel(model);

        //end
        // personal
        daoSiswa = new DaoSiswa();
        List<Siswa> siswas = daoSiswa.finds();
        types = new String[siswas.size()];
        index = 0;

        for (Siswa p : siswas) {
            types[index] = p.getId() + " . " + p.getNama();
            ++index;
        }
        DefaultComboBoxModel model2 = new DefaultComboBoxModel(types);
        cmb_namasiswa.setModel(model2);

    }

