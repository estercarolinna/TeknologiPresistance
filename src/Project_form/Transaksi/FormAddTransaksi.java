    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        try {
            Transaksi pt = new Transaksi();
            pt.setKode(txt_kode.getText());

            StringTokenizer tkn = new StringTokenizer(cmb_namasiswa.getSelectedItem().toString(), " . ");
            Long idsiswa = Long.parseLong(tkn.nextToken());
            Siswa siswa = daoSiswa.find(idsiswa);
            pt.setSiswa(siswa);

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            Date transDate = sdf.parse(txt_tanggal.getText());
            pt.setTanggal(transDate);

            tkn = new StringTokenizer(cmb_daftartagihan.getSelectedItem().toString(), " . ");
            Long iddaftartagian = Long.parseLong(tkn.nextToken());
            DaftarTagian daftarTagian = daoDaftarTagian.find(iddaftartagian);
            pt.setDaftarTagian(daftarTagian);



            pt.setHarga(txt_harga.getText());
          

            daoTransaksi.insert(pt);
//            formTransaksi.Refresh();
            
            dispose();

        } catch (ParseException e) {
        }
    }//GEN-LAST:event_btn_saveActionPerformed

