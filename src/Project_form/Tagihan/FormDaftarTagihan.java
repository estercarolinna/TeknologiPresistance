     public void Refresh(){
        List<DaftarTagian> daftarTagians = daoDaftarTagian.finds();
        String[][] data = new String[daftarTagians.size()][2];
        String header[] = {"Kode", "Daftar Tagihan"};
        
        int row = 0;
        for (DaftarTagian record : daftarTagians){
            data[row][0]= record.getKode();
            data[row][1]= record.getTagihan();
            row++;
            
        }
        DefaultTableModel model = new DefaultTableModel(data, header);
        tbdaftartagian.setModel(model);
        
        
    }

