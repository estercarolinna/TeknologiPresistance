    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        int select = JOptionPane.showConfirmDialog(formUtama, "apakah anda yakin", "confirmasi", JOptionPane.YES_NO_OPTION);
        if (select == JOptionPane.YES_OPTION) {
            int row = tbdaftartagian.getSelectedRow();
            String selectedCode = tbdaftartagian.getValueAt(row, 0).toString();
            DaftarTagian tmp = daoDaftarTagian.find(selectedCode);

            daoDaftarTagian.delete(tmp);
            Refresh();
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

